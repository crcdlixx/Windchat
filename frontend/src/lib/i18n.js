const locale = navigator.language?.startsWith('zh') ? zh : en

export function t(key, ...args) {
  let s = locale[key] ?? en[key] ?? key
  args.forEach((a, i) => { s = s.replace(`{${i}}`, a) })
  return s
}

export function isZh() {
  return locale === zh
}
